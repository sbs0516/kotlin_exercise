package ex_round_functions

import kotlin.math.*

fun main() {
    println("${ceil(3.2)}")
    println("${ceil(3.7)}")
    println("${floor(4.3)}")
    println("${floor(4.9)}")
    println("${round(2.1)}")
    println("${round(2.7)}")
    println("${15.83.roundToInt()}")
    println("${23.58.roundToLong()}")
    println("${377842.12345.roundToLong()}")
}